// Helper functions to check holiday status
export const isHoliday = (date) => {
  const formattedDate = date.toISOString().split('T')[0]; // Format as YYYY-MM-DD
  return keralaHolidays.some(holiday => holiday.date === formattedDate);
};

export const getHolidayDetails = (date) => {
  const formattedDate = date.toISOString().split('T')[0]; // Format as YYYY-MM-DD
  return keralaHolidays.find(holiday => holiday.date === formattedDate);
};

export const isSunday = (date) => {
  return date.getDay() === 0; // 0 represents Sunday in JavaScript
};

