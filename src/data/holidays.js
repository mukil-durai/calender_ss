// Get all holiday info (including Sundays)
export const getHolidayInfo = (date) => {
  if (isSunday(date)) {
    return { name: 'Sunday', type: 'Weekly Holiday', isSunday: true };
  }
  return getHolidayDetails(date);
};

// Count upcoming holidays
export const getUpcomingHolidaysCount = (fromDate = new Date(), days = 30) => {
  const today = new Date(fromDate);
  const endDate = new Date(today);
  endDate.setDate(today.getDate() + days);
  
  const upcomingHolidays = keralaHolidays.filter(holiday => {
    const holidayDate = new Date(holiday.date);
    return holidayDate >= today && holidayDate <= endDate;
  });
  
  // Add Sundays
  let sundaysCount = 0;
  const currentDate = new Date(today);
  while (currentDate <= endDate) {
    if (currentDate.getDay() === 0) { // Sunday
      sundaysCount++;
    }
    currentDate.setDate(currentDate.getDate() + 1);
  }
  
  return {
    total: upcomingHolidays.length + sundaysCount,
    govt: upcomingHolidays.length,
    sundays: sundaysCount
  };
};
